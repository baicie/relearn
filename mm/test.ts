function initTree(val = []) {
  const res = []
  const path = [
    { name: 'ethnicityId', label: 'ethnicityName' },
    { name: 'basicPostId', label: 'basicPostName' },
    { name: 'stdPostId', label: 'stdPostName' },
    { name: 'id', label: 'positionName' },
  ]

  val.forEach((item) => {
    const index0 = res.findIndex((f) => { return f.id === item.ethnicityId })

    let temp
    if (index0 < 0) {
      // 刚开始等于-1
      temp = initRowModel(item, path, 0)
      res.push(temp)
    }
    else {
      const index1 = res[index0].children.findIndex((f) => { return f.id == item.basicPostId })
      if (index1 < 0) {
        temp = initRowModel(item, path, 1)
        res[index0].children.push(temp)
      }
      else {
        const index2 = res[index0].children[index1].children.findIndex((f) => { return f.id == item.stdPostId })
        if (index2 < 0) {
          temp = initRowModel(item, path, 2)
          res[index0].children[index1].children.push(temp)
        }
        else {
          temp = initRowModel(item, path, 3)
          res[index0].children[index1].children[index2].children.push(temp)
        }
      }
    }
  })

  return res
}

function initRowModel(item, path, level) {
  // ethnicityId-->>basicPostId-->>stdPostId-->>id层级关系，item是每项数据
  const myPath = path[level]; const nextPath = path[level + 1]
  if (myPath) {
    const res = {}
    // res里id存储了对应的id,name存储了职位名称
    res.id = item[myPath.name]
    res.name = item[myPath.label]
    res.color = item.color
    // 有nextPath则level>3，否则都小于3
    // res.value = level < 3 ? this.map[res.id].value : item.matchDegree
    res.data = item
    if (nextPath)
      res.children = [initRowModel(item, path, level + 1)]
    return res
  }
}

